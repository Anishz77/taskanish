class ArithmeticModel{
  final double first;
  final double second;

  ArithmeticModel({
    required this.first,
    required this.second,
  });

  // Substract
  double substract(){
    return first-second;
  }

  //  Add
  double add(){
    return first-second;
  }

  // Multiply
  double multiply(){
    return first*second;
  }

  // Divide
  double divide(){
    return first/second;
  }


}